function square(x, y) {
    var result = 1;

    if (y >= 0) {
        for (var i = 0; i < y; i++) {
            result *= x;
        }
        return result;
    } else {
        for (var i = 0; i > y; i--) {
            result *= x;
        }
        return 1 / result;
    }
}

